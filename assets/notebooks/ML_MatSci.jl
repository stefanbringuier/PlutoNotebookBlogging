# ‚ïî‚ïê‚ï° b87811d3-d4f4-42b6-ac4c-561738f35cf9
function variablesliders(variables)
	PlutoUI.combine() do Child
		@htl("""
		<h6>Ranges</h6>
		<ul>
		$(
		[@htl("<li>$(v[1]): $(Child(RangeSlider(v[2])))</li>") for v in variables]
		)
		</ul>
		""")
	end
end

# ‚ïî‚ïê‚ï° 00d1921e-3c43-4dcb-98c6-8249b386e993
@bind r variablesliders([
	("Years",range(minimum(data[!,:year]),maximum(data[!,:year]),step=1)),
	("Articles [%]",range(0.0,maximum(data[!,:normalized]),step=0.001))
])

# ‚ïî‚ïê‚ï° 7c4cbc96-3918-48cb-853f-5a71c037dcdd
function plotted_data(data)
	plt = plot(resolution=(675,350),
			   xlabel="Years",ylabel="% ML Articles",legend=:lt,
			   xlimits=extrema(r[1]),limits=extrema(r[2]))
	for c in unique(data[!,:category])
		plotdata = filter(row -> row.category == c,data)
		plot!(plt,plotdata[!,:year],plotdata[!,:normalized],
			  markertype=:circle,
			  markercount=extrema(r[1])[1],
			  markersize=10,label=c)
	end
	return plt
end

# ‚ïî‚ïê‚ï° 785bb400-49be-4202-92de-c67c21bbfb50
