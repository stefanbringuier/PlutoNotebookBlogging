I think Ben Blaiszik's way of plotting the normalized ML publications for a specific domain was nice, so I'll do the same however, I'm going to include engineering as well. I'll continue to exclude computer science since this is already established and will have signficant number of publications that come from the machine learning, data science, artifical intelligence communities.
