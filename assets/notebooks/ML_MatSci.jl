**Author: Stefan Bringuier**

In 2014 I started to get very interested in materials informatics and the use machine learning tools in materials science. At that time the activities were mostly driven by members and funded projects associated with the [White House Materials Genome Initiatve](https://www.mgi.gov/) that had been recently signed into law. 

