I was curious about what the trend looks like for the number of publications year-over-year for keywords "machine learning" and "materials science". It turns out that this activity is just a repeat of [Ben Blaiszik's repo](github.com/blaiszik/ml_publication_charts) work. However, I'm just going to do it here for my reference so that I can easily come back to it. 
