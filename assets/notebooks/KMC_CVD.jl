md"""
# Kinetic Monte Carlo Simulation of CVD growth
**Stefan Bringuier**

This is an implementation of the exercise discussed in the book by R. LeSar [^LeSar] in Ch. 9 sec. 9.3.2. The focus is on a reduced KMC model for chemical vapor deposition that only includes three chemical reactions. Furthermore, it treats all species in gas phase as a single type.
"""

# ╔═╡ 312bc6fc-9b20-47c9-950d-96a9dc8e08db
md"""
## Listing Reactions and States/Phases
For this KMC model there are 4 species in a specific states on the surface, noting, that:

| Species | Description | Index |
| --- | :-- | --- |
| (g) | Empty gas phase | 0 |
| A(S) | Material *A* in solid phase | 1 |
| B(S) |  Material *B* in solid phase | 2 |
| AB₂(s) | Compound  *AB₂* in  solid phase | 3 |
| *S | Surface free radical|  4 |

The first is to create a site lattice model for the surface solid-state species. It is initialized according to Fig. 9.4 p. 192 in [^LeSar]. The approach I take is to create a data type called `SiteLattice` which is used to store information about the substrate such as grid points and that chemical state. In addition the location of the surface is stored and the activity values based on possible reactions.
"""

# ╔═╡ 9724bbb3-9868-4cd5-bbff-851ae3d1493f
struct SiteLattice
	nx::Integer
	ny::Integer
	state::Array{Integer}
	activity::Array{Float64}
	surface::Vector{Integer} # y-loc index of surface
	SiteLattice(nx,ny) = begin
		act = zeros(Float64,nx,ny)
		seed = zeros(Integer,nx,ny)
		seed[1,:] .= 1
		seed[2,:] .= 2
		surf = fill(2,ny)
		new(nx,ny,seed,act,surf)
	end
end

# ╔═╡ f224198c-c583-4ddb-a8ff-6fe0507ab608
md"""
The most critical aspect of KMC is knowing all relevant reactions and their associated rates. For this model we take the reactions in Table 9.1 on p. 193 of [^LeSar]. These are fictitious numbers based on the original work of Battaile and Srolovitz [^Battaile], and are given in equations 9.17a-9.17c as shown below:

$\mathrm{*(s) + B(g)} \xrightleftharpoons[k_r^1]{k_f^1} \mathrm{B(s)}$

$\mathrm{*(s) + AB_2(g)} \xrightleftharpoons[k_r^2]{k_f^2} \mathrm{AB_2(s)}$

$\mathrm{AB_2(s)  \xrightharpoonup{k_f^2} A(s)} + \mathrm{B(s)} + \mathrm{B(g)}$

Then we define a mapping between symbol representation of the reactants and products to a integer index as shown in the table above. Here we include all species but for the KMC loop we will just look at solid state species at the surface location.

"""

# ╔═╡ 26162cd0-9080-4a09-9460-c2bdfeac4e8c
begin
	allspecies = Dict("A(s)" => 1,
				   "B(s)" => 2, 
				   "AB₂(s)" => 3, 
				   "*(s)" => 4,
				   "B(g)" => 5,
				   "AB₂(g)" => 6,
				   "(g)" => 0);
	reverse(d::Dict) = Dict(v => k for (k, v) in d)
end;

# ╔═╡ 9b99ccb3-f06f-445b-9b8b-2d96aef1393d
md"""
We can now define a reaction data type that contains details for a particular reaction. We create a new data type for each possible reaction and then can conveniently store them in an array.
"""

# ╔═╡ b8d5c5b1-4634-44bc-9511-ef7a3731a998
toindex(s::Vector{String},d::Dict) = [d[i] for i in s];

# ╔═╡ cdf18520-a626-4374-b68c-2ad3056eb736
struct Reaction
	reactants::Array{Int}
	products::Array{Int}
	forwardrate::Number
	backrate::Number
	Reaction(r::Array{String},p::Array{String},f,b) = begin
		ri = toindex(r,allspecies)
		pi = toindex(p,allspecies)
		new(ri,pi,f,b)
	end
end

# ╔═╡ e7b47a62-0484-4657-9f07-bb38490ef5e1
begin
	r1 = Reaction(["*(s)","B(g)"],["B(s)"],100,200)
	r2 = Reaction(["*(s)","AB₂(g)"],["AB₂(s)"],100,400)
	r3 = Reaction(["AB₂(s)"],["A(s)","B(s)","B(g)"],400,0)
end;

# ╔═╡ 33ffd15d-72a3-4952-ab00-e50819aeea32
md"""
## Building Activities
Then we create a function that takes a list of species we are interested in performing KMC on and build the KMC rates associated with that species.
"""

# ╔═╡ 96f585c9-4776-4cd0-8d2a-8377f933636a
begin
	function buildrates(species::Vector{Int},reactions::Vector{Reaction})
		reaction_rates = Dict();
		for s in species
			#reaction_rates[s] = 0.0
			specie_rates = Dict(:total=>0.0)
			for (i,r) in enumerate(reactions)
				if s ∈ r.reactants
					k = Symbol("f",i)
					specie_rates[k] = r.forwardrate
					specie_rates[:total] += r.forwardrate
				end
				if s ∈ r.products
					k = Symbol("b",i)
					specie_rates[k] = r.backrate
					specie_rates[:total] += r.backrate
				end
			end
			reaction_rates[s] = specie_rates
		end
		return reaction_rates
	end

	buildrates(specie::Int,reactions) = buildrates([specie],reactions)
		
end;

# ╔═╡ bc8148d5-86bd-458c-a62b-7da0e2097d33
md"""
Here we specify which species we want to track, in the example by [^LeSar] only the solid state is tracked and therefore we follow the table above.
"""

# ╔═╡ bcb45689-29fd-4148-ac49-1ccf1281e504
begin
	species = ["A(s)","B(s)","AB₂(s)","*(s)"]
	rates = buildrates( toindex(species,allspecies),[r1,r2,r3])
end

# ╔═╡ 4ba8bf23-d27b-4229-806b-bc06b49869b3
md"""
The next step is to construct the activities, which is used for sampling all possible sites. This is a key step in KMC as its what determines the probability that a site is selected for a reaction to occur. The chance of a possible reaction at the sampled site is then given by the fractional activity probability over the possible reactions.
!!! note
    We note that this is being done only for the surface sites and not the sites below the surface. Also the `xloc` and `yloc` correspond to the row and column positions, respectively
"""

# ╔═╡ 68ab17d8-5aab-4567-a811-192a88fabd73
"""
Calculate the activity for those sites at the surface. x-coordinate is the height (i-index) and y-coordinate is the width (j-index).
""" function surface_activity!(substrate::SiteLattice, rates::Dict)
	
	surfloc = collect(zip(substrate.surface,1:substrate.ny))
	for xy in surfloc
		x,y = xy[1],xy[2]
		substrate.activity[x,y] = rates[substrate.state[x,y]][:total]
	end

	#For this routine this needs to be only for surface sites
	surface_activity = begin
		[substrate.activity[i,j] for (j,i) in enumerate(substrate.surface)]
	end

	frac_activity = surface_activity ./ sum(surface_activity)
	
	return sum(surface_activity),cumsum(frac_activity)		
end;

# ╔═╡ 06ecaafc-2789-43ab-92a1-ef3ec4984477
md"""
So we can now create a function to sample a random surface site, here we follow the approach where we only sample sites along the y-direction at the surface.
"""

# ╔═╡ d5dc6a33-9aee-4de6-b268-0cd8348b0bcb
function select_surface_site(substrate::SiteLattice,
	                         siteprob::Array{T}) where T<: Number
	seed =rand()
	surfloc = collect(zip(substrate.surface,1:substrate.ny))
	yloc = 1 # index counter for site selection
	xloc = 0 # dummy initial value
	for xy in surfloc
		x,y = xy[1],xy[2]
		xloc = x
		if seed > siteprob[y]
			yloc += 1
		end
	end
	return xloc,yloc
end;

# ╔═╡ 041ceb57-509e-447e-a5b8-1e8d525abfa9
md"""
Now we need to encode our rules for the site reactions. This is the heart of KMC as well and dictates the quality of the simulation.

!!! aside
    One could imagine that with Julia metaprogramming, you could have a KMC library were you only provide the reaction equations as show above and then have the library generate the code needed to setup the rules.
"""

# ╔═╡ ef3daa0a-6555-4458-86f1-2bc9487e1487
"""
This function is "hard-coded" in that it depends on reaction equations, rates, and species modeled. For any given KMC model this is where one would modify or extend to include other events.

""" function applyrules!(substrate::SiteLattice,xloc::Int,yloc::Int,rates::Dict)
	seed = rand();
	
	specie = substrate.state[xloc,yloc] :: Int
	
	# Apply relative probability rules to select outcome. These are
	# hard coded based on the model setup. So these should change based
	# on the reactions that are of interest and to track/visualize.

	# Track only solid-state changes/rules
	# B(s)
	if specie == 2 
		substrate.state[xloc,yloc] = 4

	# AB₂(s)
	elseif specie == 3
		a1 = rates[specie][:f3]/(rates[specie][:f3]+rates[specie][:b2])
		a2 = rates[specie][:b2]/(rates[specie][:f3]+rates[specie][:b2])
		if seed < a1 # AB₂ → A(s) + B(s)
			substrate.state[xloc,yloc] = 1
			substrate.state[xloc+1,yloc] = 2
			substrate.surface[yloc] = xloc+1
		else #  AB₂ → *(s)
			substrate.state[xloc,yloc] = 4
		end
		
	# *(s)
	elseif specie == 4
		a1 = rates[specie][:f1]/(rates[specie][:f1]+rates[specie][:f2])
		a2 =rates[specie][:f2]/(rates[specie][:f1]+rates[specie][:f2])
		if seed < a1
			substrate.state[xloc,yloc] = 2
		else
			substrate.state[xloc,yloc] = 3
		end
	end
end;
	

